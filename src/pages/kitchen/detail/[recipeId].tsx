import { GetServerSideProps } from "next";
import { useRouter } from "next/router";
import colors from "../../../assets/styles";
import { route } from "../../../assets/route";
import FooterButton from "../../../components/common/FooterButton";
import Navigation from "../../../components/layout/Navigation";
import DetailBox from "../../../components/kitchen/DetailBox";
import { RecipeDataType } from "../../../interface/recipe";

interface DetailProps {
  recipeData: RecipeDataType;
}

export default function Detail({ recipeData }: DetailProps) {
  const router = useRouter();
  const recipeId = router.query?.recipeId;

  return (
    <div className="box">
      <Navigation text="상세정보" />
      <div className="image" />
      <div className="name">
        <div className="comment">{recipeData.name.comment}</div>
        <div className="title">{recipeData.name.title}</div>
      </div>
      <div className="container">
        {recipeData.ingredient.map((item, idx) => (
          <DetailBox key={idx} idx={idx} type={"ingredient"} content={item} />
        ))}
        <DetailBox
          type={"nutrient"}
          content={{
            title: "영양정보",
            data: [
              { name: "탄수화물", amount: `${recipeData.carbohydrate}` },
              { name: "단백질", amount: `${recipeData.protein}` },
              { name: "지방", amount: `${recipeData.province}` },
              { name: "총 칼로리", amount: `${recipeData.total_calorie}` },
            ],
          }}
        />
        <DetailBox
          type={"tip"}
          content={{
            title: "건강 비결",
            tip: recipeData.tip,
          }}
        />
      </div>
      <FooterButton
        onClick={() => router.push(`${route.process}${recipeId}`)}
        text="요리 시작하기"
      />
      <style jsx>{`
        .box {
          width: 350px;
        }
        .image {
          height: 390px;
          width: 390px;
          margin-top: -60px;
          margin-left: -20px;
          object-fit: cover;
          background-size: cover;
          background-image: linear-gradient(
              ${colors.grayWhite},
              20%,
              transparent,
              ${colors.grayWhite}
            ),
            url(${recipeData.image});
        }
        .name {
          display: flex;
          flex-direction: column;
          align-items: center;
          font-size: 20px;
          font-weight: 900;
          margin-top: -50px;
          margin-bottom: 32px;
        }
        .container {
          display: flex;
          flex-direction: column;
          gap: 12px;
        }
        .comment {
          color: ${colors.graySubTitle};
          font-weight: 500;
        }
      `}</style>
    </div>
  );
}

export const getServerSideProps: GetServerSideProps = async (context) => {
  const recipeData = await (
    await fetch(
      `${process.env.NEXT_PUBLIC_API_ROOT}diets/${context.params?.recipeId}`
    )
  ).json();
  return {
    props: {
      recipeData,
    },
  };
};
